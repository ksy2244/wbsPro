package com.oc;

public class OcDTO {
	private int oc_code; // 발주업체 코드
	private String oc_name; // 발주업체 이름
	private String oc_tel; // 발주업체 전화번호

	public int getOc_code() {
		return oc_code;
	}

	public void setOc_code(int oc_code) {
		this.oc_code = oc_code;
	}

	public String getOc_name() {
		return oc_name;
	}

	public void setOc_name(String oc_name) {
		this.oc_name = oc_name;
	}

	public String getOc_tel() {
		return oc_tel;
	}

	public void setOc_tel(String oc_tel) {
		this.oc_tel = oc_tel;
	}

}
